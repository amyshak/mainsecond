public class MyThirdClass {
    public static void main(String[] arg) {
        int i = 0;
        while (i < 10) {
            System.out.println("Iteration: " + i);
            i++;
        }


        int d = 0;
        do {
            System.out.println("Iteration: " + d);
            d++;
        }while (d < 10);



        for (int w = 2; w < 10; w+=2){
            System.out.println("Iteration: " +w );
        }

        for (int q = 0; q < 10; q++){
            System.out.println("Iteration: " +q );
        }

        for (int r = 3; r < 8; r++){
            System.out.println("Iteration: " +r );
        }
    }}

